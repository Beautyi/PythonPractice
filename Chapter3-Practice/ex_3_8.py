cities = ['dali', 'hangzhou', 'xian', 'guilin', 'chengdu']
print(cities)
print(sorted(cities))
print(cities)
print(sorted(cities,reverse=True))
print(cities)
cities.reverse()#反向打印
print(cities)
cities.reverse()#恢复
print(cities)
cities.sort()#按字母排序
print(cities)
cities.sort(reverse=True)#按反向字母排序
print(cities)